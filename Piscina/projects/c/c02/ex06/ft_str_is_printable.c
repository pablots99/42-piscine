int		ft_str_is_printable(char *str)
{
	int aux;

	aux = 0;
	while (str[aux] != 0)
	{
		if (str[aux] < 32 || str[aux] > 126)
		{
			return (0);
		}
		aux++;
	}
	return (1);
}
